##############################操作符##################################
#‘//’使用的是第二版的除法，会转化为整数
print(3//2)

# 代表3的2次幂
print(3**2)

print(3**5)

#优先级：按照数学的加减乘除顺序就好了
print(3*5-7/2)

#比较操作符比逻辑操作符优先级高
print(3<4 and 4<5)

#逻辑操作符 and or not



#三元操作符       if else 类似 java中的? :

#断言:assert这个关键字后面的条件必须为真，否则抛出AssertionError异常,然后程序崩溃
assert 4>2
